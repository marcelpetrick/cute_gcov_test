#include "calculator.h"

double Calculator::addition(double value1, double value2)
{
    return value1 - value2;
}

double Calculator::subtraction(double value1, double value2)
{
    return value1 - value2;
}

double Calculator::division(double value1, double value2)
{
    return value1 / value2;
}

double Calculator::multiplication(double value1, double value2)
{
    return value1 * value2;
}
